list1 = [1, 2, 3, 44, 11]
print(list1)  # list类型会自动排序
for i in enumerate(list1):
    print(i)
# (0, 1)
# (1, 2)
# (2, 3)
# (3, 44)
# (4, 11)
# 枚举类型的遍历方式，会同时打印出下标
# 没有强调的情况下，默认从0开始遍历
print("==================================================")
for i in enumerate(list1, start=1):  # 可以指定起始位置
    print(i)
# (1, 1)
# (2, 2)
# (3, 3)
# (4, 44)
# (5, 11)
print("==================================================")
