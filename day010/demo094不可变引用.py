demo1 = 11
demo2 = 22
print(id(demo1))  # 1943757521456
print(id(demo2))  # 1943757521808
# 这里实际上就是找到变量对应的内存地址
# 这里的地址都是十进制地址
demo2 = demo1
print(id(demo2))  # 1943757521456
# 将其内存地址赋值过来，就是一样的地址
print("==================================================")
demo3 = "demo3"
demo4 = "demo3"
print(id(demo3))  # 1956337763632
print(id(demo4))  # 1956337763632
# 这里的内存地址都是一样的
# 也说明了int类型的变量内存地址不同，而字符串是可以共享的
