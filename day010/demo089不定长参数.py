# 不定长参数
# 在不知道具体参数数量的情况下，可以用通配符表示，这就是不定长参数
def demo_run(*demo):
    print(demo)


print("==================================================")
demo_run(123, 123, 222, 33)  # (123, 123, 222, 33)

print("==================================================")


def demo_run2(*args):
    print(args)
    # 我们默认使用的是args
    # 因为python底层就是使用args来表示


demo_run2(11, 22, "123", [1, 2, 3, 4])
