demo1 = [11, 22, 33]
print(demo1)
print(id(demo1))  # 1909856245504
print("==================================================")
demo2 = demo1
demo1.append(123)
print(demo1)
print(id(demo1))  # 1909856245504
print("==================================================")
print(demo2)
print(id(demo2))  # 1909856245504
# 上面三个地址是一样的
# 虽然数据发生了变化，但是内存地址不变
# 这就是python中可变数据类型的变化过程
# 从这里可以看出来，数据是通过引用完成传递的
